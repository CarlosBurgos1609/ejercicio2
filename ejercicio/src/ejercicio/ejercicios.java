    private Estudiante getEstudiante(){
        
        Double nota1, nota2, nota3, promedio;
        nota1=Double.parseDouble( txtnota1.getText());
        nota2=Double.parseDouble( txtnota2.getText());
        nota3=Double.parseDouble( txtnota3.getText());
        promedio=(nota1 + nota2+ nota3)/3;
        
        return new Estudiante(txtnombre.getText(), nota1, nota2, nota3, promedio);
    }
    
    private void setEstudiante(Estudiante estudiante){
        txtnombre.setText(estudiante.getNombre());
        txtnota1.setText(estudiante.getNota_1()+"" );
        txtnota2.setText(estudiante.getNota_2()+"" );
        txtnota3.setText(estudiante.getNota_3()+"" );
        txtpromedio.setText(estudiante.getPromedio()+"" );
        
    }

