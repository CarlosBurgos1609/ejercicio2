    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        // TODO add your handling code here:
        Estudiante estudianteBuscar = this.getEstudiante();
        ArrayList<Estudiante> listaEstudiantes = this.listaEstudiantes.getListaEstudiantes();
        for (Estudiante estudiante : listaEstudiantes) {
            if (estudianteBuscar.getNombre().equals(estudiante.getNombre())) {
                this.setEstudiante(estudiante);
                return;                
            }
            
        }
    }//GEN-LAST:event_btnbuscarActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        Estudiante estudiante = this.getEstudiante();
        if (estudiante == null)
        return;
        this.setEstudiante(this.listaEstudiantes.addEstudiante(estudiante));

        JOptionPane.showMessageDialog(this, "El estudiante se agrego correctamente");


    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btncalcularprActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalcularprActionPerformed
        // TODO add your handling code here:
        Double promedioGeneral=0.0;
        ArrayList<Estudiante> listaEstudiantes = this.listaEstudiantes.getListaEstudiantes();
        for (Estudiante estudiante : listaEstudiantes) {
            promedioGeneral+=estudiante.getPromedio();           
        }
        promedioGeneral/=listaEstudiantes.size();
        txtpromediogeneral.setText(promedioGeneral + "");
        
        
    }//GEN-LAST:event_btncalcularprActionPerformed

